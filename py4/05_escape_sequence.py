# Escape sequence examples in Python

print("Hello\nWorld")       # New line
print("Hello\tWorld")       # Horizontal tab
print("This is a backslash:\\")
print("She said: \"Hi!\"")
print('It\'s a sunny day.')
print("Carriage return test: Hello\rWorld")
print("Backspace test: Hello\bWorld")
print("Form feed test: Hello\fWorld")
print("Bell sound test: \a")
print("Vertical tab test: Hello\vWorld")

# Octal and hex values
print("Octal A: \101")      # A
print("Hex A: \x41")        # A

# Raw string example
print(r"This is a raw string: Hello\nWorld")